package progetto.jdbc.model;

public class GalaxyPosition {
	
	private String galaxyName;
	private Double rah;
	private Double ram;
	private Double ras;
	private String de;
	private Double ded;
	private Double dem;
	private Double des;
	
	public String getGalaxyName() {
		return galaxyName;
	}
	public void setGalaxyName(String galaxyName) {
		this.galaxyName = galaxyName;
	}
	public Double getRah() {
		return rah;
	}
	public void setRah(Double rah) {
		this.rah = rah;
	}
	public Double getRam() {
		return ram;
	}
	public void setRam(Double ram) {
		this.ram = ram;
	}
	public Double getRas() {
		return ras;
	}
	public void setRas(Double ras) {
		this.ras = ras;
	}
	public String getDe() {
		return de;
	}
	public void setDe(String de) {
		this.de = de;
	}
	public Double getDed() {
		return ded;
	}
	public void setDed(Double ded) {
		this.ded = ded;
	}
	public Double getDem() {
		return dem;
	}
	public void setDem(Double dem) {
		this.dem = dem;
	}
	public Double getDes() {
		return des;
	}
	public void setDes(Double des) {
		this.des = des;
	}
	
}
